function ValidateInputs() {
    var returnValue = false;

    if ($("#txtQuestionPaperName").val() === "") {
        $("#lblerror").val("Please enter question paper name");
        $("#txtQuestionPaperName").focus();
        returnValue = false;
    }
    else
        if ($("#txtTotalObjectiveQuestions").val() === "") {
            $("#lblerror").val("Please enter total objective question");
            $("#txtTotalObjectiveQuestions").focus();
            returnValue = false;
        }
        else

            if ($("#txtTotalMarks").val() === "") {
                $("#lblerror").val("Total marks is mendatory");
                $("#txtTotalMarks").focus();
                returnValue = false;
            }
            else
                if ($("#txtObjectiveExamTime").val() === "") {
                    $("#lblerror").val("Please enter objective exam time");
                    $("#txtObjectiveExamTime").focus();
                    returnValue = false;
                }
                else
                    if ($("#txtPassingMarks").val() === "") {
                        $("#lblerror").val("Please enter passing marks");
                        $("#txtPassingMarks").focus();
                        returnValue = false;
                    }
                    else {
                        $("#lblerror").val("");
                        returnValue = true;
                    }

}